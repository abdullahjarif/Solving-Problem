#include<bits/stdc++.h>
using namespace std;
int main()
{
    //post & pre increment, post % pre decrement
    int a = 100;
    a--;
    cout << a << endl;
    --a;
    cout << a << endl;
    a++;
    cout << a << endl;
    ++a;
    cout << a << endl;
    --a;
    cout << a << endl;
    a--;
    cout << a << endl;
    int b = 25;
    b++;
    cout << b << endl;
    ++b;
    cout << b << endl;
    --b;
    cout << b << endl;
    b--;
    cout << b << endl;
    cout << endl;
    cout << a*b << endl;
    cout << a/b << endl;
    cout << a%b << endl;
    cout << a+b << endl;
    cout << a-b << endl;
    return 0;
}
